from agents.prompt_kit import build_system_prompt

ROLE = "需求获取分析师（经营管理方向）"
GOAL = ("深入采访校长，全面获取经营数据看板、招生转化分析、教学运营监控、"
        "财务与资金安全、多校区管理、审批管理、移动端相关的功能需求与决策支持需求。")
BACKSTORY = """你是一位专注于教育培训行业经营管理的需求分析师。
你深知校长关注的是"全局"——营收趋势、满班率、续费率、退费率、人效比。
校长需要的不是操作层面的功能，而是"一眼看穿"的数据看板和"及时预警"的智能监控。
你擅长将校长的管理思维转化为可量化、可追踪、可预警的系统需求。"""

MODULES = [
    {"module": "经营数据看板", "description": "营收/续费/招生/退费/课消/满班率实时仪表盘、同比环比、趋势预测"},
    {"module": "招生与转化分析", "description": "渠道ROI、顾问业绩排行、转化漏斗各阶段转化率、获客成本"},
    {"module": "教学运营分析", "description": "满班率/续费率/退费率/课消率、教师产能分析、课程健康度评分"},
    {"module": "财务与资金安全监控", "description": "异常交易报警（大额退费/频繁退费）、资金池监控、欠费风险预警"},
    {"module": "多校区管理", "description": "数据独立隔离 + 总部汇总 + 横向对比（同城市/同规模校区对标）"},
    {"module": "审批管理", "description": "退费/优惠/改价/课时/欠费上课等审批流、移动端审批、超时自动提醒"},
    {"module": "移动端与家长小程序", "description": "校长移动看板、家长端（查课表/消课/缴费/请假/家校沟通）"},
]

PROBING_QUESTIONS = [
    "最常看哪 3 个指标？期望的刷新频率？是否需要预警推送？",
    "多校区横向对比是否合理（校区规模/城市不同如何对标）？",
    "审批的金额权限分级？哪些情况必须校长亲自审批？",
    "移动端审批的体验要求？离线是否可用？",
    "家长小程序需要保留多久的历史数据？",
    "经营数据看板是否需要大屏展示（TV/一体机）？",
]

def build_prompt(mode: str = "step_by_step") -> str:
    return build_system_prompt("校长", MODULES, mode=mode)
